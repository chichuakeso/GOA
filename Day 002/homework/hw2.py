#შეასრულეთ ოთხი მათემატიკური მოქმედება: მიმატება, გამოკლება, გამრავლება, გაყოფა ისე რომ დაიბეჭდოს თვითონ მოქმედება და პასუხი მაგ

num1 = 20
num2 = 4
num3 = 5


#დამატება

print(str(num1) + " + " + str(num2) + " = " + str(num1 + num2))

print(str(num1) + " + " + str(num3) + " = " + str(num1 + num3))

print(str(num2) + " + " + str(num3) + " = " + str(num2 + num3))

print(str(num1) + " + " + str(num2) + " + " + str(num3) + " = " + str(num1 + num2 + num3))

# გამოკლება

print(str(num1) + " - " + str(num2) + " = " + str(num1 - num2))

print(str(num1) + " - " + str(num3) + " = " + str(num1 - num3))

print(str(num2) + " - " + str(num3) + " = " + str(num2 - num3))

print(str(num1) + " - " + str(num2) + " - " + str(num3) + " = " + str(num1 - num2 - num3))

# გამრავლება

print(str(num1) + " * " + str(num2) + " = " + str(num1 * num2))

print(str(num1) + " * " + str(num3) + " = " + str(num1 * num3))

print(str(num2) + " * " + str(num3) + " = " + str(num1 * num2))

print(str(num1) + " * " + str(num2) + " * " + str(num3) + " = " + str(num1 * num2 * num3))

# გაყოფა

print(str(num1) + " / " + str(num2) + " = " + str(num1 // num2))

print(str(num1) + " / " + str(num3) + " = " + str(num1 // num3))

print(str(num2) + " / " + str(num3) + " = " + str(num2 / num3))

print(str(num1) + " / " + str(num2) + " / " + str(num3) + " = " + str(num1 // num2 // num3))